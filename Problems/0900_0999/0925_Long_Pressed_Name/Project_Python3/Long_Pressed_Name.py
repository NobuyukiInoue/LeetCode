def loop_main(temp):
    flds = temp.replace("\"","").replace("[[","").replace("]]","").rstrip().split("],[")

    name = flds[0]
    typed = flds[1]
    print("name = ""{0}"", typed = ""{1}""".format(name, typed))

    sl = Solution()
    time0 = time.time()

    result = sl.isLongPressedName(name, typed)

    time1 = time.time()

